package main.com.ttao.tutorialspoint._09Numbers

// 返回最接近的long或int。

class _11round {
    static void main(String[] args) {
        double d = 100.675
        double e = 100.500

        float f = 100
        float g = 90f

        System.out.println(Math.round(d))
        System.out.println(Math.round(e))
        System.out.println(Math.round(f))
        System.out.println(Math.round(g))
    }
}

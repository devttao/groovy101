// 给出参数的绝对值。

class _07abs {
    static void main(String[] args) {
        Integer a = -8
        double b = -100
        float c = -90

        System.out.println(Math.abs(a))
        System.out.println(Math.abs(b))
        System.out.println(Math.abs(c))
    }

}
